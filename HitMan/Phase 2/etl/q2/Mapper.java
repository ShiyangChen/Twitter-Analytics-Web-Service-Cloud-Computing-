	private static final String SENSITIVE2 = " boob ";
	public static final String OLD_FORMAT = "E MMM dd HH:mm:ss ZZZZ yyyy";
	public static final String NEW_FORMAT = "yyyy-MM-dd HH:mm:ss";

	private static SimpleDateFormat oldSDF;
	private static SimpleDateFormat newSDF;
	private static JSONParser jsonParser = new JSONParser();
	
	public Mapper() {
		oldSDF = new SimpleDateFormat(OLD_FORMAT);
		oldSDF.setTimeZone(TimeZone.getTimeZone("UTC")); 
		newSDF = new SimpleDateFormat(NEW_FORMAT);
		newSDF.setTimeZone(TimeZone.getTimeZone("UTC")); 
	}
	public void mapper() throws IOException, java.text.ParseException{
		HashMap<String, Integer> map = new HashMap<String, Integer>();
		createMap(map);
		
		HashSet<String> set = new HashSet<String>();
		createSet(set);
		
		
		
		BufferedReader bufferedReader = 
				new BufferedReader(new InputStreamReader(System.in,"UTF-8"));

//		BufferedReader bufferedReader = 
//		new BufferedReader(new FileReader("15619f14twitter-partb-aa"));

		int score;			
		String input;
		JSONObject jsonObject;
		Date date;
		String newDateString;
		String text;
		String textScore;
		String[] words;
		while ((input = bufferedReader.readLine())!= null) {
			if(input.equals(""))
				continue;
			score = 0;
			try {
				jsonObject = (JSONObject) jsonParser.parse(input);
			}catch(ParseException e){
				continue;
			}
			date = oldSDF.parse(jsonObject.get("created_at").toString());
			newDateString = newSDF.format(date);
            
            text = (String) jsonObject.get("text");
            textScore = text.replaceAll("[^0-9a-zA-Z]+", ",");
            words = textScore.split(",");
            for(String word: words){
            	String lowWord = word.toLowerCase();
            	if(map.containsKey(lowWord))
            		score += map.get(lowWord);
            }
          
            
            
    	    Pattern ddd = Pattern.compile("[0-9a-zA-Z]+");
    	    Matcher m = ddd.matcher(text);
    	    StringBuffer newText = null;
    	    while (m.find()) {
    	    	String parseWord = m.group();
    	    	
    	    	if(set.contains(parseWord.toLowerCase())){ 		
    	    		String word = ""+parseWord.charAt(0);
    	    		for(int i =0; i < parseWord.length()-2; i++)
    	    			word += "*";
    	    		word += parseWord.charAt(parseWord.length()-1);
    	    		newText = new StringBuffer(text);
    	    		newText.replace(m.start(), m.end(), word);
    	    		text = newText.toString();
    	    		
    	    	}
    	        
    	    }
    	    
    	    text = text.replaceAll("\n", SENSITIVE1);
    	    text = text.replaceAll("\r", SENSITIVE2);
    	   
    	    JSONObject user = (JSONObject)jsonObject.get("user");
    	    String userID = (String)user.get("id_str");
    	    String tweetID = (String) jsonObject.get("id_str");
     	    System.out.println( newDateString + userID + "\t" + tweetID + ":" + Integer.toString(score) + ":" +text);
		}
	}
